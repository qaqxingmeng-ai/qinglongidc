export function mergeScoringPrompt(customPrompt?: string) {
  const normalized = customPrompt?.replace(/\r\n/g, '\n').trim() || '';
  const defaultPrompt = DEFAULT_SCORING_PROMPT.trim();

  if (!normalized) return DEFAULT_SCORING_PROMPT;
  if (normalized === defaultPrompt || normalized.startsWith(defaultPrompt)) {
    return normalized;
  }

  return `${DEFAULT_SCORING_PROMPT}\n\n# 自定义补充规则\n${normalized}`;
}
